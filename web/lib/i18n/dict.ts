const DICT: Record<Locale, Record<keyof typeof KO_RAW, string>> = { ko: KO, en: EN }

export type TKey = keyof typeof KO_RAW

/** Translate. {placeholders} substituted from `vars`. Falls back to ko, then key. */
export function translate(
  locale: Locale,
  key: TKey,
  vars?: Record<string, string | number>,
): string {
  const table = DICT[locale] ?? KO
  let s: string = (table[key] as string) ?? KO[key] ?? String(key)
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(v))
    }
  }
  return s
}
